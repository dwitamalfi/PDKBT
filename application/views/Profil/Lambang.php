                        <h5 class="title">Makna Lambang Kota Batu</h5>
                        <div class="post-content">
                            <img src="<?php echo base_url();?>assets/img/core-img/logo2.png">
                            <h6><p>&nbsp;</p>

<p><strong>&nbsp;</strong></p>

<p>1.&nbsp;<strong>Gambar Bintang</strong><br />
Melambangkan Ketuhanan Yang Maha Esa, yang bermakna meskipun berbeda suku, agama, dan pandangan hidup tetap menjunjung tinggi kerukunan umat beragama</p>

<p>&nbsp;</p>

<p>2.&nbsp;<strong>Gambar Padi dan Kapas</strong><br />
melambangkan pangan dan sandang yang terdiri dari padi berjumlah 17 dan kapas berjumlah 10 mempunyai makna tanggal dan bulan peresmian Kota Batu</p>

<p>&nbsp;</p>

<p>3.&nbsp;<strong>Gambar Gunung</strong><br />
melambangkan kekuatan dan kebesaran yaitu Kota Batu berada pada lereng Gunung Panderman, gunung Arjuno, dan Gunung Welirang yang memiliki kekayaan alam yang cukup besar terutama mata air yang menyatu menjadi sungai brantas, serta keanekaragaman flora dan fauna sehingga menjadi daya tarik wisata</p>

<p>&nbsp;</p>

<p>4.&nbsp;<strong>Gambar Keris</strong><br />
Berwarna keemasan dengan posisi tegak yang melambangkan jiwa ksatria, kekuatan, ketajaman pikir, batin dan perjuangan yang pantang menyerah serta kepribadian yang berbudaya untuk mencapai Kota Batu kedepan</p>

<p>&nbsp;</p>

<p>5.&nbsp;<strong>Gambar Rantai</strong><br />
Warna hitam yang melambangkan Persatuan dan Kesatuan dalam Negara Republik Indonesia. Rantai berjumlah tiga diartikan bahwa hubungan antara Manusia dengan Tuhan serta alam dan sesamanya adalah unsur yang tidak terpisahkan.</p>

<p>&nbsp;</p>

<p>6.&nbsp;<strong>Gambar Candi</strong><br />
Melambangkan sistem pemerintahan Kota Batu yang tertib, rapi, dan teratur.</p>

<p>&nbsp;</p>

<p>7.&nbsp;<strong>Warna Dasar Hijau</strong><br />
Dengan gambar filosofi petak-petak sawah melambangkan Kota batu adalah daerah Agraris, mengandung arti filosofi &quot;Gemah Ripah Loh Jinawi&quot; (Daerah subur) dan sebagian besar masyarakatnya bertani.</p>

<p>&nbsp;</p>

<p>8.&nbsp;<strong>Gambar Air</strong><br />
Melambangkan suber kehidupan yang lestari.</p>

<p>&nbsp;</p>

<p>9.&nbsp;<strong>Bentuk Perisai</strong><br />
Memiliki 5 sisi yang melambangkan pemerintah Kota Batu berdasarkan Pancasila sebagai Dasar negara Republik Indonesia.</p>

<p>&nbsp;</p>

<p>10.&nbsp;<strong>Warna Merah Putih</strong><br />
Melambangkan Bendera Indonesia.</p>

<p>&nbsp;</p>

<p>11.&nbsp;<strong>Tulisan Kota Batu</strong><br />
Menunjukkan sebutan bagi Kota dan Pemerintah Kota Batu.</p>

<p>&nbsp;</p>

<p>12.&nbsp;<strong>Hakaryo Guno Mamayu Bawono</strong><br />
Merupakan makna Condro Sengkolo yang mengandung arti Berkarya Guna Membangun Negara. Condro Sengkolo 1934 adalah Tahun Jawa yang merupakan peresmian Pemerintah Kota Batu dengan nilai kata : Hakaryo = 4, Guno = 3, Mamayu = 9, Bawono = 1 berjumlah 17, sebgai tanggal peresmian Kota Batu, dengan jumlah suku kata 11 bermakna dasar Hukum peresmian Kota Batu diatur dalam UUN 11 Th 2001</p>
